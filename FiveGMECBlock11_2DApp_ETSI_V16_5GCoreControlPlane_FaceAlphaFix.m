        function applyWidthTools(app)
            if ~isempty(app.PanelWidthSlider) && isvalid(app.PanelWidthSlider)
                rightWidth = round(app.PanelWidthSlider.Value);
                app.MainGrid.ColumnWidth = {'1x', rightWidth};
                if ~isempty(app.PanelWidthValueLabel) && isvalid(app.PanelWidthValueLabel)
                    app.PanelWidthValueLabel.Text = sprintf('%d px', rightWidth);
                end
            end
            if ~isempty(app.LabelWidthSlider) && isvalid(app.LabelWidthSlider) && ~isempty(app.ParamFormGrid) && isvalid(app.ParamFormGrid)
                labelWidth = round(app.LabelWidthSlider.Value);
                app.ParamFormGrid.ColumnWidth = {labelWidth, '1x'};
                if ~isempty(app.LabelWidthValueLabel) && isvalid(app.LabelWidthValueLabel)
                    app.LabelWidthValueLabel.Text = sprintf('%d px', labelWidth);
                end
            end
        end

        function onPanelWidthChanged(app, value)
            if nargin < 2 || isempty(value)
                return;
            end
            if ~isempty(app.PanelWidthSlider) && isvalid(app.PanelWidthSlider)
                app.PanelWidthSlider.Value = value;
            end
            app.applyWidthTools();
        end

        function onLabelWidthChanged(app, value)
            if nargin < 2 || isempty(value)
                return;
            end
            if ~isempty(app.LabelWidthSlider) && isvalid(app.LabelWidthSlider)
                app.LabelWidthSlider.Value = value;
            end
            app.applyWidthTools();
        end

        function resetWidthTools(app)
            if ~isempty(app.PanelWidthSlider) && isvalid(app.PanelWidthSlider)
                app.PanelWidthSlider.Value = 540;
            end
            if ~isempty(app.LabelWidthSlider) && isvalid(app.LabelWidthSlider)
                app.LabelWidthSlider.Value = 300;
            end
            app.applyWidthTools();
            if ~isempty(app.StatusLabel) && isvalid(app.StatusLabel)
                app.StatusLabel.Text = 'Width tools reset. Right panel and parameter-name column returned to clear-view defaults.';
            end
        end


