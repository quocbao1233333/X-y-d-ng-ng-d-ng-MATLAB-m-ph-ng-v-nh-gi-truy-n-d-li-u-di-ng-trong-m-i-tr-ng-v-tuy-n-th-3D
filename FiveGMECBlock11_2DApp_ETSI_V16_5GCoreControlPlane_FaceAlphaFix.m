        function delay_ms = queueDelayMM1_ms(~, lambda_pkts_s, mu_pkts_s)
            % M/M/1 queue approximation:
            % rho = lambda / mu
            % T_queue ~= 1 / (mu - lambda)
            %
            % Unit:
            % lambda, mu: packets/s
            % delay_ms  : milliseconds
            %
            % If lambda >= mu, the queue is unstable. For visualization,
            % return a large capped delay instead of Inf.
            if isempty(lambda_pkts_s) || isnan(lambda_pkts_s)
                lambda_pkts_s = 0;
            end
            if isempty(mu_pkts_s) || isnan(mu_pkts_s)
                mu_pkts_s = 0;
            end

            lambda_pkts_s = max(lambda_pkts_s, 0);
            mu_pkts_s = max(mu_pkts_s, 0);

            if mu_pkts_s <= lambda_pkts_s
                delay_ms = 10000;
            else
                delay_ms = 1000 / (mu_pkts_s - lambda_pkts_s);
            end
        end

        function state = loadState(~, pct)
            % Convert a numerical load percentage into an engineering state.
            if isempty(pct) || isnan(pct)
                state = "Unknown";
            elseif pct < 70
                state = "Normal";
            elseif pct < 100
                state = "High Load";
            else
                state = "Overloaded";
            end
        end

