        %% ============================================================
        % ANIMATION
        % =============================================================
        function runPacketAnimation(app)
            if app.IsAnimating
                app.IsAnimating = false;
                app.RunButton.Text = 'Run Packet Animation';
                return;
            end

            app.readParamsFromUI();
            app.computeScenario();
            app.drawArchitecture2D();
            app.updateDashboard();

            app.IsAnimating = true;
            app.RunButton.Text = 'Stop Animation';
            app.OutputMarker.Visible = 'off';

            ids = app.RuntimePath;
            for k = 1:numel(ids)
                if ~app.IsAnimating || ~isvalid(app.UIFigure)
                    break;
                end

                app.resetBlockStyles();
                for j = 1:k
                    app.highlightBlock(ids(j));
                end
                app.updateBlockInfo(ids(k));
                app.StatusLabel.Text = sprintf('Packet is at %s ...', char(ids(k)));
                drawnow;
                pause(0.20);

                if k < numel(ids)
                    app.animateSegment(ids(k), ids(k+1));
                end
            end

            if isvalid(app.UIFigure)
                app.OutputMarker.Visible = 'on';
                app.StatusLabel.Text = sprintf('Completed. %s generated. SLA: %s.', ...
                    char(app.Results.OutputPacketID), char(app.Results.SLAStatus));
                app.RunButton.Text = 'Run Packet Animation';
            end

            app.IsAnimating = false;
        end

        function animateSegment(app, fromID, toID)
            pts = app.segmentPoints(fromID, toID);

            for s = 1:size(pts,1)-1
                p1 = pts(s,:);
                p2 = pts(s+1,:);
                nFrames = 22;
                for f = 1:nFrames
                    if ~app.IsAnimating || ~isvalid(app.UIFigure)
                        return;
                    end
                    a = (f-1)/(nFrames-1);
                    p = (1-a)*p1 + a*p2;
                    app.PacketMarker.XData = p(1);
                    app.PacketMarker.YData = p(2);
                    drawnow limitrate;
                    pause(0.015);
                end
            end
        end

