        function computeScenario(app)
            p = app.Params;

            % ------------------------------------------------------------
            % 1) Packet-size formula
            % ------------------------------------------------------------
            payloadBits = p.PayloadBytes * 8;

            % ------------------------------------------------------------
            % 2) Slice-selection formula
            % s_i = f(type_i, D_i, R_i, P_i, E_i)
            % ------------------------------------------------------------
            selectedSlice = app.selectSlice(p.ServiceType);
            [maxDelay, minPDR] = app.getSLATargets(selectedSlice);

            % ------------------------------------------------------------
            % 3) UPF load formula
            % rho_UPF = L_UPF / C_UPF
            % ------------------------------------------------------------
            rhoUPF = p.UPF_OfferedLoad_Mbps / max(p.UPF_Capacity_Mbps, eps);
            rhoUPF_pct_formula = 100 * rhoUPF;

            % Preserve UI load as an engineering override/status indicator.
            rhoUPF_pct_ui = p.UPFLoad_pct;

            % ------------------------------------------------------------
            % 4) Processing-delay formulas
            % T_proc = C_i / f
            % ------------------------------------------------------------
            T_proc_MEC_ms = 1000 * p.TaskCycles_Gcycles / max(p.FMEC_GcyclesPerSec, eps);
            T_proc_Cloud_ms = 1000 * p.TaskCycles_Gcycles / max(p.FCloud_GcyclesPerSec, eps);

            % ------------------------------------------------------------
            % 5) Queueing formulas
            % rho = lambda / mu
            % T_queue ~= 1/(mu-lambda)
            % The app adds this theoretical queueing term to the manual
            % base queue delay so the model remains interactive.
            % ------------------------------------------------------------
            rhoMEC_queue = p.Lambda_pkts_s / max(p.MuMEC_pkts_s, eps);
            rhoCloud_queue = p.Lambda_pkts_s / max(p.MuCloud_pkts_s, eps);

            T_queue_MEC_formula_ms = app.queueDelayMM1_ms(p.Lambda_pkts_s, p.MuMEC_pkts_s);
            T_queue_Cloud_formula_ms = app.queueDelayMM1_ms(p.Lambda_pkts_s, p.MuCloud_pkts_s);

            T_queue_MEC_total_ms = p.MECQueue_ms + T_queue_MEC_formula_ms;
            T_queue_Cloud_total_ms = p.CloudQueue_ms + T_queue_Cloud_formula_ms;

            % ------------------------------------------------------------
            % 6) Total MEC / Cloud delay formulas
            % ------------------------------------------------------------
            T_MEC_ms = p.RadioDelay_ms + p.UPFDelay_ms + T_queue_MEC_total_ms + T_proc_MEC_ms;
            T_Cloud_ms = p.RadioDelay_ms + p.UPFDelay_ms + p.BackhaulDelay_ms + ...
                T_queue_Cloud_total_ms + T_proc_Cloud_ms;

            % ------------------------------------------------------------
            % 7) Energy formulas
            % E_MEC = E_RAN + P_MEC*T_proc,MEC
            % E_Cloud = E_RAN + E_Backhaul + P_Cloud*T_proc,Cloud
            % ------------------------------------------------------------
            E_MEC_J = p.RanEnergy_J + p.P_MEC_W * (T_proc_MEC_ms/1000);
            E_Cloud_J = p.RanEnergy_J + p.E_Backhaul_J + p.P_Cloud_W * (T_proc_Cloud_ms/1000);

            % ------------------------------------------------------------
            % 8) Path reliability / E2E PDR
            % PDR_E2E = PDR_RAN * A_core
            % ------------------------------------------------------------
            PDR_E2E = p.PDR * p.CoreAvailability;

            % ------------------------------------------------------------
            % 9) Cost-based route decision
            % J = alpha*T + beta*E + gamma*load
            % ------------------------------------------------------------
            costMEC = p.AlphaDelay*T_MEC_ms + p.BetaEnergy*E_MEC_J + ...
                p.GammaLoad*p.MECLoad_pct;
            costCloud = p.AlphaDelay*T_Cloud_ms + p.BetaEnergy*E_Cloud_J + ...
                p.GammaLoad*p.CloudLoad_pct;

            if p.RouteMode == "Force MEC"
                selectedTarget = "MEC";
            elseif p.RouteMode == "Force Cloud"
                selectedTarget = "Cloud";
            else
                if costMEC <= costCloud
                    selectedTarget = "MEC";
                else
                    selectedTarget = "Cloud";
                end
            end

            if selectedTarget == "MEC"
                selectedDelay_ms = T_MEC_ms;
                finalPath = ["INPUT","DATA_PLANE","MEC_PLATFORM","MEC_APP","OUTPUT"];
                routeName = "Local Breakout to MEC";
                selectedEnergy_J = E_MEC_J;
                selectedQueue_ms = T_queue_MEC_total_ms;
                selectedProc_ms = T_proc_MEC_ms;
            else
                selectedDelay_ms = T_Cloud_ms;
                finalPath = ["INPUT","DATA_PLANE","CLOUD","OUTPUT"];
                routeName = "Backhaul to Cloud";
                selectedEnergy_J = E_Cloud_J;
                selectedQueue_ms = T_queue_Cloud_total_ms;
                selectedProc_ms = T_proc_Cloud_ms;
            end

            SLAPassFlag = (selectedDelay_ms <= maxDelay) && (PDR_E2E >= minPDR);

            app.Results = struct();
            app.Results.PayloadBits = payloadBits;
            app.Results.SelectedSlice = selectedSlice;
            app.Results.SliceID = app.sliceID(selectedSlice);

            app.Results.DelayMax_ms = maxDelay;
            app.Results.MinPDR = minPDR;

            app.Results.RhoUPF = rhoUPF;
            app.Results.RhoUPF_pct_formula = rhoUPF_pct_formula;
            app.Results.RhoUPF_pct_ui = rhoUPF_pct_ui;

            app.Results.RhoMECQueue = rhoMEC_queue;
            app.Results.RhoCloudQueue = rhoCloud_queue;
            app.Results.TQueueMECFormula_ms = T_queue_MEC_formula_ms;
            app.Results.TQueueCloudFormula_ms = T_queue_Cloud_formula_ms;
            app.Results.TQueueMECTotal_ms = T_queue_MEC_total_ms;
            app.Results.TQueueCloudTotal_ms = T_queue_Cloud_total_ms;

            app.Results.TProcMEC_ms = T_proc_MEC_ms;
            app.Results.TProcCloud_ms = T_proc_Cloud_ms;
            app.Results.T_MEC_ms = T_MEC_ms;
            app.Results.T_Cloud_ms = T_Cloud_ms;

            app.Results.E_MEC_J = E_MEC_J;
            app.Results.E_Cloud_J = E_Cloud_J;
            app.Results.SelectedEnergy_J = selectedEnergy_J;
            app.Results.SelectedQueue_ms = selectedQueue_ms;
            app.Results.SelectedProc_ms = selectedProc_ms;

            app.Results.PDR_E2E = PDR_E2E;
            app.Results.CoreAvailability = p.CoreAvailability;

            app.Results.CostMEC = costMEC;
            app.Results.CostCloud = costCloud;
            app.Results.SelectedTarget = selectedTarget;
            app.Results.SelectedDelay_ms = selectedDelay_ms;
            app.Results.RouteName = routeName;
            app.Results.MECGain_pct = (T_Cloud_ms - T_MEC_ms) / max(T_Cloud_ms, eps) * 100;
            app.Results.SteeringGain_pct = (T_Cloud_ms - selectedDelay_ms) / max(T_Cloud_ms, eps) * 100;
            app.Results.SLAPass = SLAPassFlag;
            app.Results.SLAStatus = app.passFail(SLAPassFlag);
            app.Results.FinalPath = finalPath;
            app.Results.UPFState = app.loadState(max(rhoUPF_pct_formula, p.UPFLoad_pct));
            app.Results.MECState = app.loadState(p.MECLoad_pct);
            app.Results.CloudState = app.loadState(p.CloudLoad_pct);
            app.Results.OutputPacketID = "RESP-0001";

            app.RuntimePath = finalPath;
        end

