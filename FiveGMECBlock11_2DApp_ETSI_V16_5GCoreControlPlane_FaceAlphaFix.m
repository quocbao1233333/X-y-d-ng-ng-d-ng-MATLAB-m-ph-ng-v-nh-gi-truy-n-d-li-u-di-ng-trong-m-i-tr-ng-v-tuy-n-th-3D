        function p = edgePoint(app, id, side, frac)
            b = app.getBlockByID(id);
            x = b.Pos(1); y = b.Pos(2); w = b.Pos(3); h = b.Pos(4);
            frac = max(0, min(1, frac));
            switch lower(char(side))
                case 'left'
                    p = [x, y + frac*h];
                case 'right'
                    p = [x + w, y + frac*h];
                case 'top'
                    p = [x + frac*w, y + h];
                case 'bottom'
                    p = [x + frac*w, y];
                otherwise
                    p = [x + w/2, y + h/2];
            end
        end

